package practice_Loops;

public class DoWhileLoop {

    public static void main(String[] args) {

        boolean condition = false;

        for (;condition;){
            System.out.println("hello zey");
        }


        while (condition){
            System.out.println("hello nur");
        }

        do {
            System.out.println("hello kotu apo");
        }while (condition);

    }


}
